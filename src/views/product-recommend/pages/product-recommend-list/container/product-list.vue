<template>
  <ProductTableList
    :maxSelect="maxSelect"
    :loading="loading"
    :pagination="pagination"
    :dataSource="dataSource"
    :selectedIdList="selectedIdList"
    @page-change="handlePageChange"
    @select="handleSelect"
    @de-select="handleDeSelect"
  />
</template>

<script>
  import ProductTableList from '../components/product-table-list'
  import { helper } from '@/views/product-recommend/store'

  const { mapActions, mapState } = helper('recommendList/productList')

  export default {
    name: 'product-table-list-container',
    props: {
      maxSelect: {
        type: Number,
        default: 100
      },
      selectedIdList: Array
    },
    computed: {
      ...mapState({
        loading: 'loading',
        pagination: 'pagination',
        dataSource: 'list'
      })
    },
    components: {
      ProductTableList
    },
    methods: {
      ...mapActions({
        handlePageChange: 'pageChange'
      }),
      handleSelect (productList) {
        this.$emit('select', productList)
      },
      handleDeSelect (productList) {
        this.$emit('de-select', productList)
      }
    }
  }
</script>
