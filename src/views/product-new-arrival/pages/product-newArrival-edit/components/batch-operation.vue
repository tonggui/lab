<template>
  <div class="product-recommend-edit-batch-operation">
    <div>
      <Checkbox @on-change="handleSelectAll" :value="value" :indeterminate="!value && indeterminate" >全选本页</Checkbox>
    </div>
    <div>
      <span class="batch-delete" :class="{ disabled }" @click="handleDelete">批量删除商品</span>
    </div>
  </div>
</template>
<script>
  export default {
    name: 'product-recommend-edit-batch-operation',
    props: {
      value: Boolean,
      indeterminate: Boolean,
      count: Number
    },
    computed: {
      disabled () {
        return this.count <= 0
      }
    },
    methods: {
      handleSelectAll (selected) {
        this.$emit('select-all', selected)
      },
      handleDelete () {
        this.$emit('delete')
      }
    }
  }
</script>
<style lang="less" scoped>
  .product-recommend-edit-batch-operation {
    display: flex;
    padding: 18px;
    justify-content: space-between;
    vertical-align: middle;
    border-top: 1px solid @border-color-base;
    .batch-delete {
      margin-right: 16px;
      cursor: pointer;
      &:not(.disabled) {
        text-decoration: underline;
      }
      &.disabled {
        color: #CCCCCC;
        cursor: not-allowed;
      }
    }
  }
</style>
